<div class="content-list-introtext">
	<p>Ez itt az MTT vezetősége számára készített szavazóprogram.</p>
	<p>A <b>bal oldalon</b> látod a számodra elérhető aktív szavazásokat.</p>
	<p>Ha csak szavazni jöttél, akkor ezt kell használnod. A listában azokat a szavazásokat látod, amelyek:
		<ul>
			<li>még nyitottak, és nem szavaztál rájuk: menj és szavazz!</li>
			<li>még nyitottak, és már szavaztál rájuk: meg kell várnod az eredménnyel a lezárását.</li>
			<li>a legutóbbi lezártak, ekkor az eredményt látod.</li> 
		</ul>
	</p>
	<p>Ha bármilyen technikai problémád van vele, <b><a href="https://www.facebook.com/sebcsaba">sebcsabát</a></b> keresd vele.</p>
</div>

<div class="thcm-fp-title">Szavazás adminisztrálása</div>
<div class="content-list-introtext">
	<p>
		A jobb oldali menüben tudod az általad kiírt szavazásokat adminisztrálni.
	</p>
	<p>Új szavazás kiírásakor meg kell adni a következő adatokat:
		<ul>
			<li>Cím: ez fog a listában a résztvevőknek megjelenni</li>
			<li>Leírás: opcionális, ide írhatod a részleteket vele kapcsolatban</li>
			<li>Lehetséges választok: legalább kettőt meg kell adni.</li>
			<li>Publikus vagy privát-e a szavazás. Publikus szavazás eredménye tartalmazni fogja, hogy ki hogyan szavazott, míg privát szavazásnál az eredmény nem fogja tartalmazni, hogy ki hogyan szavazott.</li>
			<li>Résztvevők: a tolkien.hu felhasználói, akik szavazhatnak. Legalább három karakter begépelése után a felhasználói listából kiválasztható a keresett személy. A név mellett megjelenő x-re kattintva törölhető a résztvevő. Habár itt egy résztvevő többször is felvehető, ténylegesen csak egyszer kerül rögzítésre.</li>
		</ul>
	</p>
	<p>Még nyitott szavazással a következőket lehet tenni:
		<ul>
			<li>Módosítani a címet és a leírást</li>
			<li>További résztvevőket felvenni a szavazáshoz</li>
			<li>Lezárni. Lezárt szavazásnak csak az eredményét lehet megnézni.</li>
			<li>Törölni.</li>
		</ul>
	</p>
</div>

